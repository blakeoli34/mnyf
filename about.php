				<h3>Who is Michigan Network for Youth and Families?</h3>
				<p>The Michigan Network for Youth and Families (MNYF) is a non-profit 501(c)3 membership organization. Our mission is to strengthen Michigan's youth-in-need, their families, and our communities by providing quality support, information, training and advocacy to member organizations.</p>
				<p>MNYF provides programs for runaway and/or homeless youth, as well as street outreach that serve youth-in-need. We provide an ear that will listen, a referee to help assist with family problems, a place to stay when there are concerns about abuse, and experts on youth issues. Members of MNYF are dedicated to be there, and we are dedicated to support our members.</p>
				<h3>History of the Network</h3>
				<p>In 1974, the Runaway Plan Task Force started in response to a national crisis of runaway youth being victimized and murdered. Over thirty years, and a few name changes, the organization developed into an effective voice for runaway and homeless youth, as well as other youth services. MNYF has been successful in advocating financial support to create and increase homeless youth services and programs, and gain passage of updated emancipation laws (MCL 722.4.a). We have helped with the implementation of a statewide training and technical assistance contract. Under a grant from the Skillman Foundation, MNYF was able to develop and publish a five year report in 1995, entitled "Michigan Youth in Crisis" along with a two year follow‐up report. We continue to grow and develop our services. We look forward to expanding our services, increasing our membership and becoming a statewide resource for programs serving youth‐in‐need.</p>

				<h3>Funding</h3>
				<p>Member fees allow MNYF to participate and represent the interests of its members in a number of statewide groups. Member fees also allow MNYF to do advocacy work and education on behalf of its members, and the youth, families, and communities they serve.</p>
				<p>Members of MNYF pay dues based on their operating budget for relevant programming. Affiliate memberships are also available to supporting organizations, individuals, and youth. Additional financial support comes from foundation, state, and federal contracts for training and technical assistance to providers in Michigan.</p>
				<p>If you would like more information regarding our membership structure and purpose, <a href="#">please check our by-laws.</a></p>
